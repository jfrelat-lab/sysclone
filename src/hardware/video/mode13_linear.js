                const r8 = Math.round((r / 63) * 255);
                const g8 = Math.round((g / 63) * 255);
                const b8 = Math.round((b / 63) * 255);
                this.palette32[i] = (0xFF << 24) | (b8 << 16) | (g8 << 8) | r8;
